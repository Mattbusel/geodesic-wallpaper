/// A rendered glyph as a row-column bit matrix.
#[derive(Debug, Clone)]
pub struct GlyphBitmap {
    pub width: u8,
    pub height: u8,
    pub rows: Vec<u8>,
}

impl GlyphBitmap {
    /// Return true if pixel at (row, col) is set.
    pub fn pixel(&self, row: u8, col: u8) -> bool {
        if row >= self.height || col >= self.width { return false; }
        let shift = self.width - 1 - col;
        (self.rows[row as usize] >> shift) & 1 == 1
    }
}

pub struct FontRenderer;

impl FontRenderer {
    fn glyph_data(c: char) -> &'static [u8; 7] {
        let idx = c as usize;
        if idx >= 32 && idx <= 126 {
            &FONT_5X7[idx - 32]
        } else {
            &FONT_5X7[0] // space fallback
        }
    }

    /// Render a single character to a GlyphBitmap (5w x 7h).
    pub fn render_char(c: char, _color: [u8; 3]) -> GlyphBitmap {
        GlyphBitmap {
            width: 5,
            height: 7,
            rows: Self::glyph_data(c).to_vec(),
        }
    }

    /// Render a full string to a 2D RGB pixel grid.
    pub fn render_text(text: &str, color: [u8; 3], bg: Option<[u8; 3]>) -> Vec<Vec<[u8; 3]>> {
        let bg_color = bg.unwrap_or([0, 0, 0]);
        let (w, h) = Self::text_size(text);
        if w == 0 { return vec![]; }
        let mut img = vec![vec![bg_color; w]; h];

        let mut x_off = 0usize;
        for c in text.chars() {
            let glyph = Self::render_char(c, color);
            for row in 0..7usize {
                for col in 0..5usize {
                    if glyph.pixel(row as u8, col as u8) {
                        if x_off + col < w {
                            img[row][x_off + col] = color;
                        }
                    }
                }
            }
            x_off += 6; // 5px glyph + 1px kerning gap
        }
        img
    }

    /// Blit `text_bitmap` onto `image` at position (x, y), clipping at bounds.
    pub fn blit(
        image: &mut Vec<Vec<[u8; 3]>>,
        text_bitmap: &Vec<Vec<[u8; 3]>>,
        x: usize,
        y: usize,
    ) {
        let img_h = image.len();
        let img_w = if img_h > 0 { image[0].len() } else { return };
        for (row_idx, row) in text_bitmap.iter().enumerate() {
            let dest_row = y + row_idx;
            if dest_row >= img_h { break; }
            for (col_idx, &px) in row.iter().enumerate() {
                let dest_col = x + col_idx;
                if dest_col >= img_w { break; }
                image[dest_row][dest_col] = px;
            }
        }
    }

    /// Return (width, height) in pixels for the given text string.
    pub fn text_size(text: &str) -> (usize, usize) {
        let len = text.chars().count();
        if len == 0 { return (0, 0); }
        // Each char is 5 wide + 1 gap, last char has no trailing gap.
        let w = len * 5 + (len - 1);
        (w, 7)
    }
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_font_array_size() {
        assert_eq!(FONT_5X7.len(), 95);
    }

    #[test]
    fn test_glyph_bitmap_pixel_a() {
        let glyph = FontRenderer::render_char('A', [255, 255, 255]);
        assert_eq!(glyph.width, 5);
        assert_eq!(glyph.height, 7);
        // Row 0 of 'A' is 0x04 = 00100 â€” centre pixel set
        assert!(glyph.pixel(0, 2));
        // Column 0 should be off in row 0
        assert!(!glyph.pixel(0, 0));
    }

    #[test]
    fn test_glyph_pixel_out_of_bounds() {
        let glyph = FontRenderer::render_char('A', [255, 0, 0]);
        assert!(!glyph.pixel(10, 0));
        assert!(!glyph.pixel(0, 10));
    }

    #[test]
    fn test_text_size_single() {
        let (w, h) = FontRenderer::text_size("A");
        assert_eq!(w, 5);
        assert_eq!(h, 7);
    }

    #[test]
    fn test_text_size_multi() {
        let (w, h) = FontRenderer::text_size("AB");
        assert_eq!(w, 11); // 5 + 1 + 5
        assert_eq!(h, 7);
    }

    #[test]
    fn test_text_size_empty() {
        assert_eq!(FontRenderer::text_size(""), (0, 0));
    }

    #[test]
    fn test_render_text_dimensions() {
        let bmp = FontRenderer::render_text("Hi", [255, 0, 0], None);
        assert_eq!(bmp.len(), 7);
        assert_eq!(bmp[0].len(), 11);
    }

    #[test]
    fn test_blit_basic() {
        let mut img = vec![vec![[0u8; 3]; 20]; 20];
        let text = FontRenderer::render_text("A", [255, 255, 255], None);
        FontRenderer::blit(&mut img, &text, 0, 0);
        // Centre pixel of row 0 of 'A' should be white
        assert_eq!(img[0][2], [255, 255, 255]);
    }

    #[test]
    fn test_blit_clipped_at_boundary() {
        let mut img = vec![vec![[0u8; 3]; 4]; 4];
        let text = FontRenderer::render_text("HELLO", [255, 0, 0], None);
        // Should not panic even though text is wider than image
        FontRenderer::blit(&mut img, &text, 0, 0);
    }

    #[test]
    fn test_blit_offset() {
        let mut img = vec![vec![[0u8; 3]; 30]; 20];
        let text = FontRenderer::render_text("Z", [0, 255, 0], Some([0, 0, 0]));
        FontRenderer::blit(&mut img, &text, 10, 5);
        // Spot check: no panic and dimensions unchanged
        assert_eq!(img.len(), 20);
        assert_eq!(img[0].len(), 30);
    }

    #[test]
    fn test_render_char_space() {
        let glyph = FontRenderer::render_char(' ', [255, 255, 255]);
        // Space is all zeros
        for r in 0..7 {
            for c in 0..5 {
                assert!(!glyph.pixel(r, c));
            }
        }
    }
}